CREATE TABLE CAD_SETOR (
    ID_SETOR       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SET_COD        VARCHAR2(3)  NOT NULL UNIQUE,
    SET_DESCRICAO  VARCHAR2(30) NOT NULL,
    SET_ATIVO      CHAR(1) DEFAULT 'S' CHECK (SET_ATIVO IN ('S','N'))
);

CREATE TABLE CAD_EQUIPAMENTO (
    ID_EQUIPAMENTO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EQU_SUB_TAG    VARCHAR2(6)  NOT NULL UNIQUE,
    EQU_FABRICANTE VARCHAR2(30) NOT NULL,
    EQU_MODELO     VARCHAR2(30) NOT NULL,
    EQU_VIDA_UTIL  NUMBER       NOT NULL,
    EQU_FREQ_LUB   NUMBER       NOT NULL,
    EQU_RPM        NUMBER       NOT NULL,
    EQU_RPM_FIXO   CHAR(1)      NOT NULL CHECK (EQU_RPM_FIXO IN ('S','N'))
);

CREATE TABLE CAD_COLABORADOR (
    ID_COLABORADOR NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    COL_ID_SETOR   NUMBER NOT NULL,
    COL_COD        NUMBER NOT NULL UNIQUE,
    COL_NOME       VARCHAR2(40) NOT NULL,
    COL_FUNCAO     VARCHAR2(30) NOT NULL,
    COL_ATIVO      CHAR(1) DEFAULT 'S' CHECK (COL_ATIVO IN ('S','N')),
    CONSTRAINT FK_COL_SETOR FOREIGN KEY (COL_ID_SETOR)
        REFERENCES CAD_SETOR (ID_SETOR)
);

CREATE TABLE CAD_POSICAO (
    ID_POSICAO        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    POS_ID_SETOR      NUMBER NOT NULL,
    POS_ID_EQUIP      NUMBER NOT NULL,
    POS_TAG           VARCHAR2(10) NOT NULL UNIQUE,
    POS_DESCRICAO     VARCHAR2(50) NOT NULL,
    POS_DT_INSTALACAO DATE NOT NULL,
    POS_TEMP_BASE     NUMBER NOT NULL,
    POS_DT_PROX_LUB   DATE,
    POS_ATIVO         CHAR(1) DEFAULT 'S' CHECK (POS_ATIVO IN ('S','N')),
    CONSTRAINT FK_POS_SETOR FOREIGN KEY (POS_ID_SETOR)
        REFERENCES CAD_SETOR (ID_SETOR),
    CONSTRAINT FK_POS_EQUIP FOREIGN KEY (POS_ID_EQUIP)
        REFERENCES CAD_EQUIPAMENTO (ID_EQUIPAMENTO)
);

CREATE TABLE CAD_TP_ALERTA (
    ID_TP_ALERTA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TPA_DESCRICAO VARCHAR2(20) NOT NULL,
    TPA_GRAU      NUMBER NOT NULL,
    TPA_ATIVO     CHAR(1) DEFAULT 'S' CHECK (TPA_ATIVO IN ('S','N'))
);

CREATE TABLE MAN_ROT_TEMP (
    ID_ROT_TEMP      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    RTE_ID_POS       NUMBER NOT NULL,
    RTE_ID_COL       NUMBER NOT NULL,
    RTE_DATA         DATE DEFAULT SYSDATE NOT NULL,
    RTE_TEMP_AMB     NUMBER NOT NULL,
    RTE_TEMP_EQUIP   NUMBER NOT NULL,
    RTE_VELOCIDADE   NUMBER NOT NULL,
    RTE_OBS          VARCHAR2(100),
    CONSTRAINT FK_RTE_POS FOREIGN KEY (RTE_ID_POS)
        REFERENCES CAD_POSICAO (ID_POSICAO),
    CONSTRAINT FK_RTE_COL FOREIGN KEY (RTE_ID_COL)
        REFERENCES CAD_COLABORADOR (ID_COLABORADOR)
);

CREATE TABLE MAN_ROT_LUB (
    ID_ROT_LUB  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    RLU_ID_POS  NUMBER NOT NULL,
    RLU_ID_COL  NUMBER NOT NULL,
    RLU_DATA    DATE DEFAULT SYSDATE NOT NULL,
    RLU_OBS     VARCHAR2(100),
    CONSTRAINT FK_RLU_POS FOREIGN KEY (RLU_ID_POS)
        REFERENCES CAD_POSICAO (ID_POSICAO),
    CONSTRAINT FK_RLU_COL FOREIGN KEY (RLU_ID_COL)
        REFERENCES CAD_COLABORADOR (ID_COLABORADOR)
);

CREATE TABLE MAN_OCORRENCIAS (
    ID_OCORRENCIA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    OCO_ID_POS    NUMBER NOT NULL,
    OCO_DATA      DATE DEFAULT SYSDATE NOT NULL,
    OCO_OBS       VARCHAR2(150) NOT NULL,
    CONSTRAINT FK_OCO_POS FOREIGN KEY (OCO_ID_POS)
        REFERENCES CAD_POSICAO (ID_POSICAO)
);

CREATE TABLE MAN_FALHA (
    ID_FALHA           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FAL_ID_POS         NUMBER NOT NULL,
    FAL_ID_R_TEMP      NUMBER,
    FAL_ID_R_LUB       NUMBER,
    FAL_ID_OCORRENCIA  NUMBER,
    FAL_ID_TP_ALERTA   NUMBER NOT NULL,
    FAL_DATA           DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_FAL_POS FOREIGN KEY (FAL_ID_POS)
        REFERENCES CAD_POSICAO (ID_POSICAO),
    CONSTRAINT FK_FAL_RTE FOREIGN KEY (FAL_ID_R_TEMP)
        REFERENCES MAN_ROT_TEMP (ID_ROT_TEMP),
    CONSTRAINT FK_FAL_RLU FOREIGN KEY (FAL_ID_R_LUB)
        REFERENCES MAN_ROT_LUB (ID_ROT_LUB),
    CONSTRAINT FK_FAL_OCO FOREIGN KEY (FAL_ID_OCORRENCIA)
        REFERENCES MAN_OCORRENCIAS (ID_OCORRENCIA),
    CONSTRAINT FK_FAL_TPA FOREIGN KEY (FAL_ID_TP_ALERTA)
        REFERENCES CAD_TP_ALERTA (ID_TP_ALERTA)
);

CREATE TABLE FUZZY_INFERENCIA (
    ID_REGRA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    T_AMB    VARCHAR2(15) NOT NULL,
    T_EQUIP  VARCHAR2(15) NOT NULL,
    VELOC    VARCHAR2(15) NOT NULL,
    LUB      VARCHAR2(15) NOT NULL,
    T_MAQ    VARCHAR2(15) NOT NULL,
    STATUS   VARCHAR2(15) NOT NULL
);

CREATE INDEX IDX_RTE_POS_DATA ON MAN_ROT_TEMP (RTE_ID_POS, RTE_DATA);
CREATE INDEX IDX_RLU_POS_DATA ON MAN_ROT_LUB (RLU_ID_POS, RLU_DATA);
CREATE INDEX IDX_POS_EQUIP    ON CAD_POSICAO (POS_ID_EQUIP);
CREATE INDEX IDX_FUZZY_STATUS ON FUZZY_INFERENCIA (STATUS);

